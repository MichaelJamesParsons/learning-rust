
fn main () {
    // All variables are immutable by default.
    let mut immutable_var: i32 = 10;

    // Mutable variable.
    let mut mutable_variable: i32 = 20;

    // Boolean
    let is_true: bool = true;
    let is_true_short = true;

    // Char
    let some_char: char = 'x';
    let some_char_short = 'x';

    // Multiple assignment
    let(f_name, l_name) = ("Michael", "Parsons");
}